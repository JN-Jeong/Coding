def solution():
    n = int(input())

    def add_star(ptn):
        # print("#", type(ptn), ptn)
        return "* " + ptn + " *"

    def star(n):
        if n == 1:
            return ["*"]
        else:
            # fmt:off
            return (
                ["*" * (4 * n - 3), 
                 "*" + " " * (4 * n - 5) + "*"]
                + list(map(add_star, star(n - 1)))
                + ["*" + " " * (4 * n - 5) + "*", 
                   "*" * (4 * n - 3)]
            )
            # fmt:on

    print("\n".join(star(n)))


if __name__ == "__main__":
    solution()
